for (const [language, messages] of Object.entries(supplementalDictionaries) as Array<[UILanguage, MessageMap]>) {
  Object.assign(dictionaries[language], messages);
}

export function translate(language: string | null | undefined, key: string, params: Record<string, string | number> = {}) {
  const uiLanguage = getUILanguage(language);
  const value = dictionaries[uiLanguage][key] ?? en[key] ?? key;
  if (Array.isArray(value)) return value[Math.floor(Math.random() * value.length)];
  return typeof value === 'function' ? value(params) : value;
}

export function useI18n() {
  const nativeLanguage = useUIStore((state) => state.nativeLanguage);
  return useMemo(
    () => ({
      language: getUILanguage(nativeLanguage),
      nativeLanguage,
      t: (key: string, params?: Record<string, string | number>) => translate(nativeLanguage, key, params),
    }),
    [nativeLanguage],
  );
}
