function traditionalizeText(text: string) {
  return traditionalReplacements.reduce((value, [from, to]) => value.replaceAll(from, to), text);
}

function traditionalize(map: MessageMap): MessageMap {
  return Object.fromEntries(
    Object.entries(map).map(([key, value]) => [
      key,
      typeof value === 'function'
        ? (params: Record<string, string | number>) => traditionalizeText(value(params))
        : Array.isArray(value)
          ? value.map(traditionalizeText)
          : traditionalizeText(value),
    ]),
  );
}

